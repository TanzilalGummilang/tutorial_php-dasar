<?php 
// pengulangan
// for, while, do while, foreach (pengulangan khusus array)

	// for
	for($i=0; $i<5; $i++){
		echo "heloo broo! <br>";
	};
	echo "<br>";

	// while
	$i=0;
	while( $i<5 ){
		echo "salam sejahtera! <br>";
		$i++;
	};
	echo "<br>";

	// do while
	$i=0;
	do{
		echo "gud morning! <br>";
		$i++;
	} while( $i<5 );
	echo "<br>";

 ?>